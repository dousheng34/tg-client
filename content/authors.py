# Авторлар тізімі
AUTHOR_KEYS = list(AUTHORS.keys())

def get_author(key: str) -> dict:
    return AUTHORS.get(key, {})

def get_authors_by_grade(grade: int) -> list:
    result = []
    for key, data in AUTHORS.items():
        if grade in data.get('grade', []):
            result.append((key, data['name'], data['emoji']))
    return result

def format_author_card(key: str) -> str:
    a = AUTHORS.get(key)
    if not a:
        return "Автор табылмады."
    return (
        f"{a['emoji']} <b>{a['name']}</b>\n"
        f"📅 {a['years']}\n"
        f"🏡 {a['born']}\n\n"
        f"{a['short_bio']}"
    )

def format_author_full(key: str) -> str:
    a = AUTHORS.get(key)
    if not a:
        return "Автор табылмады."
    works = "\n".join(f"  • {w}" for w in a['works'])
    quotes = "\n".join(f"  💬 {q}" for q in a['quotes'])
    facts = "\n".join(a['facts'])
    return (
        f"{'='*35}\n"
        f"{a['emoji']} <b>{a['name']}</b>\n"
        f"{'='*35}\n"
        f"📅 <b>Жылдары:</b> {a['years']}\n"
        f"🏡 <b>Туған жер:</b> {a['born']}\n\n"
        f"📖 <b>Өмірбаяны:</b>\n{a['full_bio']}\n\n"
        f"📚 <b>Шығармалары:</b>\n{works}\n\n"
        f"💬 <b>Цитаталары:</b>\n{quotes}\n\n"
        f"📌 <b>Қызықты деректер:</b>\n{facts}"
    )

def format_infographic(key: str) -> str:
    a = AUTHORS.get(key)
    if not a:
        return "Автор табылмады."
    return f"<code>{a['infographic']}</code>"
