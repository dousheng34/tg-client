def get_grade_info(grade: int) -> dict:
    return GRADE_CONTENT.get(grade, {})


def get_grade_topics(grade: int) -> list:
    data = GRADE_CONTENT.get(grade, {})
    return data.get('topics', [])


def format_grade_menu(grade: int) -> str:
    data = GRADE_CONTENT.get(grade, {})
    if not data:
        return "Сынып табылмады."
    topics = data.get('topics', [])
    topics_text = "\n".join(f"  {i+1}. {t['title']}" for i, t in enumerate(topics))
    return (
        f"{data['emoji']} <b>{data['title']}</b>\n\n"
        f"📚 Тақырыптар:\n{topics_text}"
    )


def get_topic_content(grade: int, topic_key: str) -> str:
    topics = get_grade_topics(grade)
    for t in topics:
        if t['key'] == topic_key:
            return t['content']
    return "Тақырып табылмады."
