def get_extended_themes(grade_num: int):
    """Сынып нөмірі бойынша 100 тақырып қайтарады"""
    return EXTENDED_THEMES.get(grade_num, [])


def count_themes(grade_num: int):
    """Тақырып санын қайтарады"""
    return len(get_extended_themes(grade_num))


def all_themes_count():
    """Барлық сыныптардың тақырып санын қайтарады"""
    return {g: len(t) for g, t in EXTENDED_THEMES.items()}
