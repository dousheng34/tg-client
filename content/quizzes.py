def get_quizzes_for_grade(grade: int, count: int = 10) -> list:
    """Берілген класс үшін сұрақтар алу"""
    pool = [q for q in QUIZZES["all"] if grade in q.get('grade', [])]
    if not pool:
        pool = QUIZZES["all"]
    random.shuffle(pool)
    return pool[:min(count, len(pool))]


